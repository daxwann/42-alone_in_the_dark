#include <stdio.h>

void	ft_add(int a, int b)
{
	printf("%d\n", a + b);
}

void	ft_minus(int a, int b)
{
	printf("%d\n", a - b);
}

void	ft_times(int a, int b)
{
	printf("%d\n", a * b);
}

void	ft_divide(int a, int b)
{
	printf("%d\n", a / b);
}

void	ft_mod(int a, int b)
{
	printf("%d\n", a % b);
}
