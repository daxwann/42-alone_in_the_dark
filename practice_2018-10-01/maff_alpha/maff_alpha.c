#include <unistd.h>

int	main(void)
{
	int a;
	int b;

	a = 'a';
	while (a <= 'z')
	{
		if (a % 2 == 0)
		{
			b = a - 'a' + 'A';
			write(1, &b, 1);
		}
		else
			write(1, &a, 1);
		a++;
	}
	write(1, "\n", 1);
	return (0);
}
